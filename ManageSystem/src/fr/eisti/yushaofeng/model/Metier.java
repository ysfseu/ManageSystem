package fr.eisti.yushaofeng.model;

public class Metier {
	int id;
	String name;
	int droitId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDroitId() {
		return droitId;
	}
	public void setDroitId(int droitId) {
		this.droitId = droitId;
	}

}
